package jdbcOperations;

public class ImageDetails {

	private int id;
	private String imageName;
	public ImageDetails(){
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getImageName() {
		return imageName;
	}

	public void setImageName(String imageName) {
		this.imageName = imageName;
	}
	
}
